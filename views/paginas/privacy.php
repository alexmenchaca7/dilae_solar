        <h3>1. ¿Qué datos personales recabamos?</h3>
        <p>Su información personal será utilizada para las finalidades señaladas en este aviso. Para ello, requerimos obtener los siguientes datos personales:</p>
        <ul>
            <li><strong>Formulario de Contacto:</strong> Nombre, correo electrónico, teléfono, código postal, horario de preferencia y el mensaje que usted nos envíe.</li>
            <li><strong>Calculadora Solar:</strong> Tipo de tarifa de CFE y gasto bimestral promedio (o los gastos de los últimos 6 bimestres) para generar una estimación.</li>
            <li><strong>Suscripción al Boletín:</strong> Correo electrónico.</li>
            <li><strong>Datos recopilados automáticamente:</strong> Información sobre su visita a nuestro sitio a través de nuestro sistema de analítica (analytics.dilaesolar.com) y cookies necesarias para el funcionamiento del sitio y del chat en vivo.</li>
        </ul>

        <h3>2. ¿Con qué fines utilizamos sus datos personales?</h3>
        <p>Los datos personales que recabamos de usted, los utilizaremos para las siguientes finalidades:</p>
        <h4>Finalidades Primarias (necesarias para el servicio):</h4>
        <ul>
            <li>Responder a sus mensajes y solicitudes enviadas a través del formulario de contacto.</li>
            <li>Generar y mostrarle una estimación de ahorro y características de un sistema solar a través de nuestra calculadora.</li>
            <li>Verificar la validez de su código postal para ofrecer un servicio más preciso.</li>
            <li>Gestionar su suscripción a nuestro boletín informativo y enviarle comunicaciones relacionadas.</li>
            <li>Brindar soporte y atención al cliente a través de nuestro chat en vivo.</li>
        </ul>
        <h4>Finalidades Secundarias (opcionales):</h4>
        <ul>
            <li>Analizar el comportamiento de los usuarios en nuestro sitio web para mejorar nuestros servicios y la experiencia de usuario.</li>
        </ul>

        <h3>3. Derechos ARCO (Acceso, Rectificación, Cancelación y Oposición)</h3>
        <p>
            Usted tiene derecho al Acceso, Rectificación y Cancelación de sus datos personales, a Oponerse al tratamiento de los mismos o a revocar el consentimiento que para dicho fin nos haya otorgado. Para ello, es necesario que envíe la solicitud en los términos que marca la Ley en su Art. 29 al correo electrónico <strong>contacto@dilaesolar.com</strong>, el cual solicitamos confirme vía telefónica para garantizar su correcta recepción.
        </p>

        <h3>4. Transferencia de datos personales</h3>
        <p>Hacemos de su conocimiento que sus datos podrán ser transferidos a las siguientes entidades para cumplir con las finalidades descritas:</p>
        <ul>
            <li><strong>API de COPOMEX:</strong> Para validar la existencia de los códigos postales ingresados en nuestro formulario de contacto.</li>
            <li><strong>Servicio de Chat en Vivo (im_livechat):</strong> Para proporcionar el servicio de chat en nuestro sitio web. Al interactuar con el chat, usted comparte información directamente con este servicio.</li>
            <li><strong>Proveedor de Servicios de Correo Electrónico:</strong> Para el envío de correos transaccionales (respuestas a formularios, confirmación de suscripción).</li>
        </ul>
        <p>Si usted no manifiesta su oposición, se entenderá que ha otorgado su consentimiento para estas transferencias.</p>

